const letterPositions = function(sentence) {
  const results = {};

  for (let i = 0; i < sentence.length; i++) {
    const letter = sentence[i];
    if (letter !== " ") {
      if (Object.keys(results).includes(letter)) {
        results[letter].push(i);
      } else {
        results[letter] = [i];
      }
    }
  }

  return results;
}

